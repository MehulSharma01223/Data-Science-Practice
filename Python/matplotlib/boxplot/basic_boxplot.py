import matplotlib.pyplot as plt

# =========================
# DATA
# =========================

marks = [
    45, 50, 52, 55, 60,
    65, 67, 70, 72, 75,
    78, 80, 82, 85, 88,
    90, 92, 95, 150
]

# =========================
# FIGURE SIZE
# =========================

plt.figure(figsize=(8,5))

# =========================
# BOXPLOT
# =========================

plt.boxplot(marks)

# =========================
# TITLE
# =========================

plt.title("Marks Boxplot")

# =========================
# Y LABEL
# =========================

plt.ylabel("Marks")

# =========================
# GRID
# =========================

plt.grid(True)

# =========================
# SAVE GRAPH
# =========================

# plt.savefig("basic_boxplot.png")

# =========================
# SHOW GRAPH
# =========================

plt.show()